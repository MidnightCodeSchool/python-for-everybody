# Calculate area of a rectangle
width = input("ความกว้างของสี่เหลี่ยมผืนผ้าเท่าไหร่? (cm) ")
height = input("ความสูงของสี่เหลี่ยมผืนผ้าเท่าไหร่? (cm) ")
width = float(width)
height = float(height)
area_of_rectangle = width * height
print("พื้นที่ของสี่เหลี่ยมผืนผ้าคือ " + str(area_of_rectangle) + " ตารางเซนติเมตร")